package prueba.bus;

public class Pasajeros {
	String nombre;
	String apellido;
	String deporteQuePractica;
	int codigoSeguroSocial;
	String empresaQueTrabaja;
	
	public int getCodigoSeguroSocial() {
		return codigoSeguroSocial;
	}
	public void setCodigoSeguroSocial(int codigoSeguroSocial) {
		this.codigoSeguroSocial = codigoSeguroSocial;
	}
	public String getEmpresaQueTrabaja() {
		return empresaQueTrabaja;
	}
	public void setEmpresaQueTrabaja(String empresaQueTrabaja) {
		this.empresaQueTrabaja = empresaQueTrabaja;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getApellido() {
		return apellido;
	}
	public void setApellido(String apellido) {
		this.apellido = apellido;
	}
	public String getDeporteQuePractica() {
		return deporteQuePractica;
	}
	public void setDeporteQuePractica(String deporteQuePractica) {
		this.deporteQuePractica = deporteQuePractica;
	}
	
	
}
